# A for loop is used to iterate over a sequence (like a list, string, tuple, dictionary keys, or range of numbers) 
# a fixed number of times.
# It is generally used when the number of iterations is known beforehand.

for i in range(5):
    print(i)

# Here, the number of iterations is known beforehand → 5 times
# This is why for is used when the number of repeats is fixed